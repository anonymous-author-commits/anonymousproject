"""Bootstrap CI for the 5x5 cross-payoff Spearman matrix.

Reads results/cpr_panel.csv (one row per (run, city) cell, with
columns cpr_{payoff}_{sampler}). Resamples (run, city) cells with
replacement B times, recomputes the 5x5 Spearman matrix on each
resample, and reports (point, CI_lo, CI_hi) per cell.

Outputs
-------
    results/cross_payoff_rho_ci.json -- per-pair {point, lo, hi}
    tables/cross_payoff_matrix_ci.tex -- LaTeX tabular replacing the
                                         point-only matrix
"""
from __future__ import annotations

import csv
import json
import sys
from pathlib import Path

import numpy as np

ROOT = Path(__file__).resolve().parents[1]
RESULTS = ROOT / "results"
TABLES = ROOT / "tables"

PAYOFFS = ["wire", "routing", "coverage", "dc_flow", "n_minus_1"]
SAMPLERS = ["indep", "corr"]
B_BOOT = 10000
SEED = 0


def _spearman(a: np.ndarray, b: np.ndarray) -> float:
    if a.size < 2 or a.size != b.size:
        return float("nan")
    ar = np.argsort(np.argsort(a)).astype(float)
    br = np.argsort(np.argsort(b)).astype(float)
    ar -= ar.mean()
    br -= br.mean()
    denom = np.sqrt((ar * ar).sum() * (br * br).sum())
    return float((ar * br).sum() / denom) if denom > 0 else float("nan")


def _matrix_for_resample(rows: list[dict]) -> dict[tuple[str, str], float]:
    def _to_float(x):
        try: return float(x) if x != "" else float("nan")
        except (TypeError, ValueError): return float("nan")
    out: dict[tuple[str, str], float] = {}
    for p1 in PAYOFFS:
        for p2 in PAYOFFS:
            rhos = []
            for s in SAMPLERS:
                v1 = np.array([_to_float(r[f"cpr_{p1}_{s}"]) for r in rows])
                v2 = np.array([_to_float(r[f"cpr_{p2}_{s}"]) for r in rows])
                m = ~(np.isnan(v1) | np.isnan(v2))
                if m.sum() >= 2:
                    rho = _spearman(v1[m], v2[m])
                    if not np.isnan(rho):
                        rhos.append(rho)
            out[(p1, p2)] = float(np.mean(rhos)) if rhos else float("nan")
    return out


def main() -> int:
    csv_path = RESULTS / "cpr_panel.csv"
    if not csv_path.exists():
        print(f"[error] {csv_path} not found.")
        return 1
    with csv_path.open("r", encoding="utf-8") as f:
        rows = list(csv.DictReader(f))
    n = len(rows)
    print(f"Loaded {n} cells from {csv_path}.")

    # Point estimate.
    point = _matrix_for_resample(rows)

    # Bootstrap.
    rng = np.random.default_rng(SEED)
    samples: dict[tuple[str, str], list[float]] = {
        (p1, p2): [] for p1 in PAYOFFS for p2 in PAYOFFS
    }
    print(f"Bootstrapping B={B_BOOT} ...")
    for b in range(B_BOOT):
        idx = rng.integers(0, n, size=n)
        rs = [rows[i] for i in idx]
        m = _matrix_for_resample(rs)
        for k, v in m.items():
            samples[k].append(v)
        if (b + 1) % 1000 == 0:
            print(f"  ... {b+1}/{B_BOOT}")

    summary: dict[str, dict[str, float]] = {}
    for (p1, p2), vals in samples.items():
        arr = np.asarray([v for v in vals if not np.isnan(v)])
        if arr.size == 0:
            lo, hi = float("nan"), float("nan")
        else:
            lo = float(np.percentile(arr, 2.5))
            hi = float(np.percentile(arr, 97.5))
        summary[f"{p1}__{p2}"] = {
            "point": float(point[(p1, p2)]),
            "ci_lo": lo,
            "ci_hi": hi,
            "n_boot_valid": int(arr.size),
        }
    out = RESULTS / "cross_payoff_rho_ci.json"
    with out.open("w", encoding="utf-8") as f:
        json.dump(summary, f, indent=2)
    print(f"Wrote {out}")

    # Render LaTeX table with CIs.
    pretty = {"wire": "Wire", "routing": "Routing", "coverage": "Coverage",
              "dc_flow": "DC-flow", "n_minus_1": "$N{-}1$"}
    TABLES.mkdir(parents=True, exist_ok=True)
    tex = TABLES / "cross_payoff_matrix_ci.tex"
    with tex.open("w", encoding="utf-8") as f:
        f.write("% Auto-generated by experiments/bootstrap_cross_payoff.py\n")
        n_p = len(PAYOFFS)
        f.write("\\renewcommand{\\arraystretch}{1.25}\n")
        f.write("\\arrayrulecolor{NEband}\n")
        f.write("\\begin{tabular}{l " + " ".join(["c"] * n_p) + "}\n")
        f.write("\\toprule\n")
        f.write("\\rowcolor{NEblue}\n")
        f.write("\\color{white}\\textbf{Payoff}")
        for p in PAYOFFS:
            f.write(f" & \\color{{white}}\\textbf{{{pretty[p]}}}")
        f.write(" \\\\\n")
        f.write("\\midrule\n")
        for p1 in PAYOFFS:
            f.write(pretty[p1])
            for p2 in PAYOFFS:
                if p1 == p2:
                    f.write(" & --- ")
                else:
                    s = summary[f"{p1}__{p2}"]
                    f.write(f" & \\shortstack{{{s['point']:+.3f}\\\\"
                            f"\\scriptsize[{s['ci_lo']:+.2f},"
                            f"{s['ci_hi']:+.2f}]}}")
            f.write(" \\\\\n")
        f.write("\\bottomrule\n")
        f.write("\\end{tabular}\n")
        f.write("\\arrayrulecolor{black}\n")
    print(f"Wrote {tex}")

    print("\n=== Cross-payoff matrix with 95% bootstrap CI ===")
    for p1 in PAYOFFS:
        line = [f"{p1:>10s}"]
        for p2 in PAYOFFS:
            s = summary[f"{p1}__{p2}"]
            if p1 == p2:
                line.append(f"{'---':>22s}")
            else:
                line.append(
                    f"{s['point']:+.3f} [{s['ci_lo']:+.2f},{s['ci_hi']:+.2f}]"
                )
        print("  " + "  ".join(line))
    return 0


if __name__ == "__main__":
    raise SystemExit(main())
