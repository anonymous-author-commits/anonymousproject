"""Translate routing CPR into excess-capex bands per city.

Reads the existing per-cell panel data
(`results/cpr_panel.csv` and `results/cpr_multiseed_summary.csv`)
and emits a per-city table of "best-vs-worst non-degenerate
generator" excess capex in USD millions.

Outputs
-------
* results/monetary_table.csv -- per (city, voltage_class) excess
  capex bands with best/worst generator names.
* tables/monetary_summary.tex -- LaTeX-formatted table for the
  paper's new §2.3.

The "best" generator is the one with the lowest mean routing CPR
on that city across the multiseed runs; the "worst non-degenerate"
explicitly excludes v1 and dist_v1 (documented degenerate runs in
the upstream DUPT report).
"""
from __future__ import annotations

import csv
import sys
from pathlib import Path

ROOT = Path(__file__).resolve().parents[1]
if str(ROOT) not in sys.path:
    sys.path.insert(0, str(ROOT))

from cpr.monetary import best_vs_worst_capex, COST_PER_KM_USD_M  # noqa: E402

RESULTS = ROOT / "results"
TABLES = ROOT / "tables"

# Generators that are NOT non-degenerate (excluded from "worst")
DEGENERATE_RUNS = {"v1", "dist_v1"}
# Synthetic baselines also excluded from "best/worst non-degenerate
# generator" framing -- they are reference distributions, not generators.
BASELINE_RUNS = {"baseline_random", "baseline_perturbed"}
# Non-DUPT but deterministic; included as candidate "worst" since they are
# proposed as alternatives in some grids.
NON_DUPT_DETERMINISTIC = {"voronoi_density", "mst_substations"}


def _read_multiseed_summary() -> list[dict]:
    p = RESULTS / "cpr_multiseed_summary.csv"
    rows = []
    with p.open("r", encoding="utf-8") as f:
        for r in csv.DictReader(f):
            if r["payoff"] != "routing":
                continue
            try:
                rows.append({
                    "run": r["run"],
                    "city": r["city"],
                    "mean": float(r["mean_cpr"]),
                    "ci_lo": float(r["ci_lo_95"]),
                    "ci_hi": float(r["ci_hi_95"]),
                })
            except (KeyError, ValueError):
                continue
    return rows


def main() -> int:
    rows = _read_multiseed_summary()
    by_city: dict[str, list[dict]] = {}
    for r in rows:
        if r["run"] in BASELINE_RUNS or r["run"] in DEGENERATE_RUNS:
            continue
        by_city.setdefault(r["city"], []).append(r)

    out_rows = []
    for city, cands in sorted(by_city.items()):
        cands_sorted = sorted(cands, key=lambda x: x["mean"])
        best = cands_sorted[0]
        # Worst: pick the worst of the non-degenerate DUPT generators only,
        # not the deterministic non-DUPT (those are alternatives, not failures).
        dupt = [c for c in cands_sorted if c["run"] not in NON_DUPT_DETERMINISTIC]
        worst = dupt[-1] if dupt else cands_sorted[-1]

        capex = best_vs_worst_capex(
            best["mean"], worst["mean"],
            voltage_class="HV", n_actions=4,
        )
        print(f"[{city:>10s}]  best={best['run']:>16s} ({best['mean']:6.2f})  "
              f"worst-non-degenerate={worst['run']:>16s} ({worst['mean']:6.2f})  "
              f"->  {capex}")

        out_rows.append({
            "city": city,
            "best_run": best["run"],
            "best_cpr": best["mean"],
            "worst_run": worst["run"],
            "worst_cpr": worst["mean"],
            "delta_cpr_px": worst["mean"] - best["mean"],
            "excess_km_per_action": capex.excess_km_per_action,
            "capex_low_USDm": capex.low,
            "capex_mid_USDm": capex.midpoint,
            "capex_high_USDm": capex.high,
        })

    # Write CSV
    csv_path = RESULTS / "monetary_table.csv"
    with csv_path.open("w", newline="", encoding="utf-8") as f:
        w = csv.DictWriter(f, fieldnames=list(out_rows[0].keys()))
        w.writeheader()
        for r in out_rows:
            w.writerow(r)
    print(f"\nWrote {csv_path}")

    # Write LaTeX table for the paper.
    TABLES.mkdir(parents=True, exist_ok=True)
    tex_path = TABLES / "monetary_summary.tex"
    with tex_path.open("w", encoding="utf-8") as f:
        f.write("% Auto-generated by experiments/monetary_translation.py\n")
        f.write("\\renewcommand{\\arraystretch}{1.25}\n")
        f.write("\\arrayrulecolor{NEband}\n")
        f.write("\\begin{tabular}{l c c r}\n")
        f.write("\\toprule\n")
        f.write("\\rowcolor{NEblue}\n")
        f.write("\\color{white}\\textbf{City} & "
                "\\color{white}\\textbf{Best gen.\\ vs.\\ worst non-degen.} & "
                "\\color{white}\\textbf{Excess km / action} & "
                "\\color{white}\\textbf{Excess HV capex (USD M)} \\\\\n")
        f.write("\\midrule\n")
        for r in out_rows:
            city = r["city"].replace("_", "\\_")
            f.write(f"{city} & {r['best_run']} \\textit{{vs.}} "
                    f"{r['worst_run']} & "
                    f"{r['excess_km_per_action']:.2f} & "
                    f"{r['capex_low_USDm']:.1f}--{r['capex_high_USDm']:.1f} "
                    f"(mid {r['capex_mid_USDm']:.1f}) \\\\\n")
        f.write("\\bottomrule\n")
        f.write("\\end{tabular}\n")
        f.write("\\arrayrulecolor{black}\n")
    print(f"Wrote {tex_path}")

    # Aggregate stat for paper text.
    total_low = sum(r["capex_low_USDm"] for r in out_rows)
    total_mid = sum(r["capex_mid_USDm"] for r in out_rows)
    total_high = sum(r["capex_high_USDm"] for r in out_rows)
    print(f"\nAggregate over {len(out_rows)} cities (HV, K=4):")
    print(f"  total excess capex band: ${total_low:.1f}M - ${total_high:.1f}M  "
          f"(midpoint ${total_mid:.1f}M)")

    # Single-city headline (Chicago, since it's mentioned by reviewer).
    chi = next((r for r in out_rows if r["city"] == "chicago"), None)
    if chi:
        print(f"\nChicago headline:")
        print(f"  excess routing distance: {chi['excess_km_per_action']:.2f} km / action")
        print(f"  capex range: ${chi['capex_low_USDm']:.1f}-{chi['capex_high_USDm']:.1f}M")
        print(f"  midpoint at $2.5M/km HV: ${chi['capex_mid_USDm']:.1f}M")

    return 0


if __name__ == "__main__":
    raise SystemExit(main())
